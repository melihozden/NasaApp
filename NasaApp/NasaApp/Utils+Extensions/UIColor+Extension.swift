import Foundation
import UIKit

extension UIColor {
    public class var smokeBlack: UIColor{
        return UIColor(red: 2/255, green: 2/255, blue: 2/255, alpha: 1.0)  //#020202
    }
    
    public class var smokeWhite: UIColor{
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1.0)  //#F5F5F5
    }
}
